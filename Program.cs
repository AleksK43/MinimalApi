using Microsoft.EntityFrameworkCore;
using Microsoft.EntityFrameworkCore.InMemory;
using WebApplication1.Models; 

var builder = WebApplication.CreateBuilder(args);

// Add services to the container.
// Learn more about configuring Swagger/OpenAPI at https://aka.ms/aspnetcore/swashbuckle
builder.Services.AddEndpointsApiExplorer();
builder.Services.AddSwaggerGen();
builder.Services.AddDbContext<TodoDb>(opt => opt.UseInMemoryDatabase("TodoList"));

var app = builder.Build();

// Configure the HTTP request pipeline.
if (app.Environment.IsDevelopment())
{
    app.UseSwagger();
    app.UseSwaggerUI();
}

app.UseHttpsRedirection();


app.MapGet("/GetProvinces", async (TodoDb db) =>
    await db.Provinces.ToListAsync());

app.MapGet("/GetProvince/{Id}", async (int id, TodoDb db) =>
    await db.Provinces.FindAsync(id)
        is Province province
        ? Results.Ok(province)
        : Results.NotFound());

app.MapPost("/ProvinceItems", async (Province province, TodoDb db) =>
{
    db.Provinces.Add(province);
    await db.SaveChangesAsync();
    return Results.Created($"/ProvinceItems/{province.Id}", province); 
});
     
